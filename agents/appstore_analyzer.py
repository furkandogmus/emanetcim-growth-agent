def fetch_reviews(app_id, country="us", count=20):
    """
    Fetch App Store reviews.
    Uses the Apple RSS feed (no API key needed).
    """
    headers = {
        "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36",
    }

    # Apple RSS review feed format
    # Note: This is a public feed; real app IDs needed for production
    url = f"https://itunes.apple.com/{country}/rss/customerreviews/id={app_id}/sortBy=mostRecent/json"

    try:
        req = urllib.request.Request(url, headers=headers)
        with urllib.request.urlopen(req, timeout=10) as r:
            data = json.loads(r.read().decode("utf-8", errors="replace"))
    except Exception as e:
        return [], str(e)

    reviews = []
    try:
        feed = data.get("feed", {})
        entries = feed.get("entry", [])
        for entry in entries:
            if "author" in entry:  # Skip the first entry which is metadata
                review = {
                    "title": entry.get("title", {}).get("label", ""),
                    "content": entry.get("content", {}).get("label", ""),
                    "rating": entry.get("im:rating", {}).get("label", "0"),
                    "author": entry.get("author", {}).get("name", {}).get("label", ""),
                    "date": entry.get("updated", {}).get("label", ""),
                }
                reviews.append(review)
                if len(reviews) >= count:
                    break
    except (KeyError, IndexError, TypeError) as e:
        return [], f"Parse error: {e}"

    return reviews, None

def analyze_sentiment(reviews):
    """Simple keyword-based sentiment and topic analysis."""
    complaints = []
    praises = []
    feature_requests = []
    pricing_mentions = []

    complaint_keywords = [
        "expensive", "price", "cost", "too much", "refund", "cancel",
        "broken", "bug", "crash", "error", "slow", "confusing",
        "bad", "terrible", "awful", "worst", "disappointed",
        "support", "customer service", "help", "refund",
    ]

    praise_keywords = [
        "great", "amazing", "excellent", "awesome", "best", "love",
        "perfect", "wonderful", "fantastic", "incredible", "easy",
        "convenient", "fast", "secure", "reliable", "helpful",
    ]

    for review in reviews:
        content = review["content"].lower() + " " + review["title"].lower()
        rating = int(review["rating"])

        # Check for complaints
        for kw in complaint_keywords:
            if kw in content:
                complaints.append(review)
                break

        # Check for praises
        for kw in praise_keywords:
            if kw in content:
                praises.append(review)
                break

        # Check for pricing mentions
        if any(w in content for w in ["€", "$", "£", "price", "cost", "fee", "ücret", "fiyat"]):
            pricing_mentions.append(review)

        # Check for feature requests
        if any(w in content for w in ["wish", "would be great", "need", "feature", "add", "option"]):
            feature_requests.append(review)

    return complaints, praises, pricing_mentions, feature_requests

